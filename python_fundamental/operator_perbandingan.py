# operator perbandingan
# > lebih besar dari
print(1 > 2) # false
# < lebih kecil dari
print(1 < 2) # true
# >= lebih dari sama dengan 
print(2 >= 2) #true
# <= lebih kecil sama dengan
print(4 <= 4) # true
# == sama dengan
print(5 == 3) #false
# != tidak sama dengan
print(5 != 3) #true
print("danil" != "danil") #false