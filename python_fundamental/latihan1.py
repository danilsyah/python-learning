# section 1.2 create variabel and assigning values

# integer
a = 2
print(a, "type = ", type(a))

# float
pi = 3.14
print(pi, "type = ", type(pi))

# string
c = 'A'
print(c, "type = ", type(c))

name = 'Danil syah'
print(name , "type = ", type(name))

# boolean
q = True
q1 = False
print(q, "type = ", type(q))
print(q1, "type = ", type(q))

# empty value or null data type
x = None
print("x value = ",x , "type = ", type(x))
